// Ejemplo de uso de arreglos en JavaScript

// Los arreglos o arrays son estructuras que permiten almacenar multiples valores en una sola variable.
// Cada valor dentro del arreglo se identifica por un numero llamado "indice" o "subindice".

// ⚠️ Importante:
// Los indices en JavaScript comienzan desde 0 (cero).
// Es decir, el primer elemento del arreglo esta en la posicion 0, el segundo en la posicion 1, y asi sucesivamente.

// Por ejemplo:
var ejemplo = ["a", "b", "c"];
// ejemplo[0] devuelve "a"
// ejemplo[1] devuelve "b"
// ejemplo[2] devuelve "c"

// Ahora los arreglos originales:

// Arreglo 1: Lista del supermercado
var listaSupermercado = ["arroz", "frijoles", "lentejas", "pan"];
// listaSupermercado[0] → "arroz"
// listaSupermercado[3] → "pan"

// Arreglo 2: Gastos en numeros
var gastos = [100, 142, 231];
// gastos[1] → 142

// Arreglo 3: Informacion de contacto
var infoContacto = ["Diego", "Rivera", "Guanajuato", "Mexico", 812345678];
// infoContacto[2] → "Guanajuato"
// infoContacto[4] → 812345678